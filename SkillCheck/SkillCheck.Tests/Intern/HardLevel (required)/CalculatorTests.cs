using System.Data;
using System;
using Xunit;


public class Calculator
{
    public int Calculate(string equation)
    {
        var dataTable = new System.Data.DataTable();
        return Convert.ToInt32(dataTable.Compute(equation, string.Empty));
    }
}



public class CalculatorTests
{
    /*
     * Здесь тест необходимо только раскомментировать
     */
    private readonly Calculator _calculator = new Calculator();

    [Theory]
    [InlineData("1+1", 2)]
    [InlineData("2-1", 1)]
    [InlineData("3*2", 6)]
    [InlineData("6/3", 2)]
    [InlineData("2+3*4", 14)]
    [InlineData("(2+3)*4", 20)]
    [InlineData("10/2+5", 10)]
    [InlineData("5-3*2+8/4", 1)]
    [InlineData("10*(2+3)-4/2", 58)]
    [InlineData("15-3*(2*5)/6+6", 4)]
    public void Calculate_equation_of_string_and_return_result_of_calculate(string equation, int expectedResult)
    {
        var actualResult = _calculator.Calculate(equation);

        Assert.Equal(expectedResult, actualResult);
    }
}
